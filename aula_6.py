# Coerção - conversão de tipos :
# Type convertion, typecasting, coercion.
# É o ato de converter um tipo em outro.
# tipos imutáveis e primitivos : str, int, float, bool.

print (int ('1'), type (int('1'))) #Aqui, estamos dizendo que o a string (') é inteiro, por meio do type INT (type servindo para mostrar a class)

print (int ('1') + 1) #Já aqui, dissemos que a string ('1') é inteiro, assim, somando com o outro int (1)

print (float ('1.2') + 1) #Aqui, a mesma coisa de o item acima, porém com tipo float. 

print (bool ('')) #Aqui, a conversão de uma string para boolean, se tiver vazia é false, e com QUALQUER coisa é TRUE

print (str (11) + 'b') #Conversão de uma string. 


