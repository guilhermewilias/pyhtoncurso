# Titulo da aula : Operadores aritimeticos da matématica >

# Adição = +
# Subtração = -
# Multiplicação = *
# Divisão = /
# Divisão inteira = //
# Exponenciação = **
# Modulo % (Resto da divisão)

var_adicao = 10 + 10
print ('Resultado da adição (10+10) ->', var_adicao)

var_subtracao = 10 -5 
print ('\nResultado da subtração -> ', var_subtracao)

var_multiplicacao = 10 * 5
print ('\nResultado da multiplicação -> ', var_multiplicacao)

var_divisao = 10 / 2.2
print ('\nResultado da divisão -> ', var_divisao)

var_divisao_inteira = 10 // 2.2
print ('\nResultado da divisão inteira -> ', var_divisao_inteira)

var_exponente = 2 ** 10
print ('\nResultado da exponenciação -> ', var_exponente)

var_modulo = 55 % 2 #(Resto da divisão)
print ('\nResultado do modulo -> ', var_modulo)

