# Função do IF / ELIF / ELSE 
# Traduc -> SE / SE NÃO SE / SE NÃO



var_entrada = input ('você quer entrar ou sair? ')




if var_entrada == 'entrar':  # Se digitado ENTRAR pelo user ira printar a msg abaixo 
    print ('você entrou no sistema')


elif var_entrada == 'sair': # Se digitado SAIR pelo user ira printar a msg abaixo 
    print ('você saiu do sistema')


else :  #Se o user n digitar nem entrar nem sair irá aparecer a imagem abaixo 
    print ('você não digitou nem entrar nem sair')




