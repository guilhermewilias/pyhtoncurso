# Titulo da aula : INT e FLOAT

# Int = número inteiro.
# Int representa qualquer numero.
# Int sem sinal é considerado positivo.
print (11)
print (-11)
print (0)
print ("\n")

# o print ("\n") Apenas pulou uma linha.

# Float = número com ponto flutuante 
# Float representa qualquer numero
# Float sem sinal é considerado positivo
# Sempre utilizar ponto (.) e não vírgula (,)
print (1.1)
print (0.0)
print (-1.1)

#A função type () mostra o tipo de classe do caracter inserido >
print ("\n")
